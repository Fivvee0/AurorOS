#include <types.h>

int power(int base, int exp) {
    int result = 1;
    for (int i = 0; i < exp; i++) {
        result *= base;
    }
    return result;
}

int highest_power_of_two(int n) {
    int power = 1;
    while (power <= n) power <<= 1;
    return power >> 1;
}